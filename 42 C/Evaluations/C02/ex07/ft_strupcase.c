char	*ft_strupcase(char *str)
{
	char	*temp;

	while (*str)
	{
		temp = str;
		if (*str >= 'a' && *str <= 'z')
		{
			*str = *str - 32 ;
		}
		str++;
	}
	return (temp);
}
