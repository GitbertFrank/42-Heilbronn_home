#include <unistd.h>

int	ft_strlen(char *str)
{
	int	len;

	len = 0;
	while (str[len])
		len++;
	return (len);
}

int	main(int argc, char **argv)
{
	argc = ft_strlen(*argv);
	write(1, *argv, argc);
	write(1, "\n", 1);
}