#include <unistd.h>

void	ft_print_numbers(void)
{
	char	digits[10];
	int		i;
	char	c;

	i = 0;
	c = 48;
	while (c < 58)
	{
		digits[i] = c;
		c++;
		i++;
	}
	write(1, &digits, 10);
}
