void	clear_grid(int grid[4][4])
{
	int	y;
	int	x;

	y = 0;
	x = 0;
	while (y < 4)
	{
		x = 0;
		while (x < 4)
		{
			grid[y][x] = 0;
			x++;
		}
		y++;
	}
}
